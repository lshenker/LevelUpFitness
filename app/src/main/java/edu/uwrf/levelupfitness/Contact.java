package edu.uwrf.levelupfitness;

public class Contact {

    public String id, userName, pass, firstName, lastName, height,
            weight, phone, WO1, WO2, WO3;

    // setters
    public void setId(String id){
        this.id = id;
    }
    public void setUsername(String userName){
        this.userName = userName;
    }
    public void setPass(String pass){
        this.pass = pass;
    }
    public void setFirstName(String firstName){
        this.firstName = firstName;
    }
    public void setLastName(String lastName){
        this.lastName = lastName;
    }
    public void setHeight(String height){
        this.height = height;
    }
    public void setWeight(String weight){
        this.weight = weight;
    }
    public void setPhone(String phone){
        this.phone = phone;
    }
    public void setWO1(String WO1){
        this.WO1 = WO1;
    }
    public void setWO2(String WO2){
        this.WO2 = WO2;
    }
    public void setWO3(String WO3){
        this.WO3 = WO3;
    }


    // getters
    public String getId(){
        return this.id;
    }
    public String getUserName(){
        return this.userName;
    }
    public String getPass(){
        return this.pass;
    }
    public String getFirstName(){
        return this.firstName;
    }
    public String getLastName(){
        return this.lastName;
    }
    public String getHeight(){
        return this.height;
    }
    public String getWeight(){
        return this.weight;
    }
    public String getPhone(){
        return this.phone;
    }
    public String getWo1(){
        return this.WO1;
    }
    public String getWO2(){
        return this.WO2;
    }
    public String getWO3(){
        return this.WO3;
    }


}
